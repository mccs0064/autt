    /* global $, window */

    $(function () {
        'use strict';

        // Initialize the jQuery File Upload widget:
        $('#fileupload').fileupload({

            url: '<?php echo Yii::app()->createUrl("fleetmanager/uploads/myphotos");?>'
        });

            // Load existing files:
            $('#fileupload').addClass('fileupload-processing');
            $.ajax({
                // Uncomment the following to send cross-domain cookies:
                //xhrFields: {withCredentials: true},
                url: $('#fileupload').fileupload('option', 'url'),
                dataType: 'json',
                context: $('#fileupload')[0]
            }).always(function () {
                $(this).removeClass('fileupload-processing');
            }).done(function (result) {
                $(this).fileupload('option', 'done')
                    .call(this, $.Event('done'), {result: result});
            });

    });
    $(document.body).on('click','.delete-myvehiclephoto',function(){
        var $this=$(this);
        var filePath=$(this).closest('.template-download').find('td').eq(1).find("a").attr("href");
        var type='myPhotos';
        $.ajax({
            type: 'post',
            url: '<?php echo Yii::app()->createUrl("fleetmanager/addaccident/removephoto");?>',
            data: {filePath: filePath,type: type},
            success: function(resp)
            {
                var response=$.parseJSON(resp);
                if(response.status==true)
                {
                    $this.closest('.template-download').remove();
                }
            }
        });
    });



</script>
